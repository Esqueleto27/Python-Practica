# Conversores de temperatura

celcius = float(input("Ingrese sus grados en celcius: "))
fahrenhait = celcius * 9/5 + 32
kelvin = celcius + 273.15

print(f"""
      Los grados en celcius es: {celcius},
      Los grados en fahrenhait es: {fahrenhait},
      Los grados en Kelvin es; {kelvin} 
      """)

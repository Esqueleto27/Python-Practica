# Metodos de imprimir los strings en minusculas, mayusculas, etc

texto = "mAtheo fLores"
print(texto)

# Mayusculas
mayusculas = texto.upper()
print(mayusculas)

# Minusculas
minuculas = texto.lower()
print(minuculas)

# Primera letra del string en mayuscula
primera_mayuscula = texto.capitalize()
print(primera_mayuscula)

# Sin espacios
parrafo = ("hola mundo")
print(parrafo)
sin_espacios = parrafo.replace(" ", "")
print(sin_espacios)
